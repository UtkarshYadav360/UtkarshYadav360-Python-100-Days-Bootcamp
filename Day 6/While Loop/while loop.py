a = 0

# loop will run till the 'a' becomes greater than or equals to 10
while a<=10 :
    # print 'a' till the above condition becomes False
    print(a)
    # adding 1 to 'a' each time the loop runs, so the value of 'a' will increase eaach time the loop will run
    a+=1
