# TYPE CHECKING :

# type() funciton is used to check the data type
name = len("Utkarsh")
print(type(name))



# TYPE CONVERSION :

# str() function change the data type into string
name_char = len(input("What is your name? "))
print("Your name has " + str(name_char) + " characters.")

# int() function change the data type into integer
a = "123"
a = int(a)
print('"123" string changed to',type(a))

a = float(a)
print('"123" string changed to',type(a))
