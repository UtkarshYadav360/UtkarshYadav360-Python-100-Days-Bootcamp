# ACCESSING STRING VALUES :

# Python counting starts from 0
print("Hello"[0]) # H
print("Hello"[4]) # o

# the output will be a concatinated string
print("123" + "345") # 123345

print()





# PYTHON BASIC DATA TYPES :

# STRING DATA TYPE :
# anything inside "double quotes" or 'single quotes' is a string
print("string inside double quotes")
print('string inside single quotes')



# INTEGER DATA TYPE :
print(123 + 345) # 468

# large numbers can be written by using underscores _
print(123_345 + 123_345)



# FLOAT DATA TYPE :
# numbers with decimals are the floating point numbers
print(3.14 + 3.06) # 6.2



# BOOLEAN DATA TYPE :
# True and False are the boolean data types
print(True)
print(False)