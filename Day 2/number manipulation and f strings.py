# round() FUNCTION :

# rounds the number to the specified numbers after the decimal point
a = round(2.6666666,2) # 2.67
print(type(a)) # float



# FLOOR DIVISION :
b = 18//7 # 2
print(type(b)) # int



# we can also perform mathematical operations by using the variable name
num = 45
num//=5
print(num) # 9



# F STRINGS :
score = 0
score+=10
print(f"Your score is {score}.")
