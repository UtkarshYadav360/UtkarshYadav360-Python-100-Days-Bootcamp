# BAND NAME GENERATOR :

# TAKE INPUT FROM THE USER FOR THE CITY WHERE THEY GREW UP IN AND TAKE INPUT FROM THE USER FOR THEIR PET'S NAME.
# MEREGE BOTH INPUTS AND PRINT THE BAND NAME.

# THE INPUT SHOULD BE IN THE NEW LINE






# greeting to the user
print("WELCOME TO THE BAND NAME GENERATOR")

# user city input
city = input("Enter the city where your grew up in :\n")

# user pet's name input
pet_name = input("Enter the name of your pet :\n")

# merging both inputs
band_name = city + " " + pet_name

# final output
print("Your band name could be",band_name)
