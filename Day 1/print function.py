# string are created inside "double quotes"
print("Hello World!")

# strings are also created using 'single quotes'
print('Hello World!')


# we can use "double quotes" inside 'single quotes'
print('Hello "World!" In Double Quotes')

# we can use 'single quotes' inside "double quotes"
print("Hello 'World' In Single Quotes")

""" "double quotes "cannot be used inside double quotes"" and,
'single quotes 'cannot be used inside single quotes'' """
