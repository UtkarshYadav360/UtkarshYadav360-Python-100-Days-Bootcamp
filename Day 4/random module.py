# RANDOM MODULE :


# importing random module
import random

# generating a random integer between a specific range
rand_int = random.randint(1,10) # 1 and 10 are included
print(rand_int)



# geneating a random float number
rand_float = random.random() # generates a random number between 0 and 1
print(rand_float)



# generating a random float number between 0 and 5
rand_float_num = random.random()*5 # generates a random number between 0 and 5
print(rand_float_num)
